#클래스 기반 프로그래밍 == 객체지향 기법
#클래스 라는건 왜 만들었나..> 초보자 한테 일을 시키려고 만든 문법이다.(단, 코드 공개는 안하고)

def Abssum(n1,n2):
    result = 0 #>>>함수가 끝나면 없어질 공간
    if n1<0:
        n1 = n1*-1
    if n2<0:
        n2=n2*-1
    result =n1+n2
    return result

#초보 개발자는 변수를 만들어야 하고 
#함수를 적절히 사용해야 하고
#print까지 시켜줘야한다.
result1 = Abssum(-1,3)
print(result1)

